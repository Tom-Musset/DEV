//recuperation du level choisi
function levelChoose(){
    const choix = document.querySelectorAll('.item')

    for(let i=0;i<choix.length;i++){
        choix[i].addEventListener('click', function(){
            levelId = choix[i].id
            switch(levelId){
                case 'level1':
                    level = 1
                    cardArray = cardArray1
                    cardsWon=[]
                    resultDisplay.textContent = cardsWon.length
                    cardsChosen=[]
                    cardsChosenIds=[]
                    createBoard()
                    console.log('le niveau choisi est le 1')
                    break
                case 'level2':
                    level = 2
                    cardArray = cardArray2
                    cardsWon=[]
                    resultDisplay.textContent = cardsWon.length
                    cardsChosen=[]
                    cardsChosenIds=[]
                    createBoard()
                    console.log('le niveau choisi est le 2')
                    break
                case 'level3':
                    level = 3
                    cardArray = cardArray3
                    cardsWon=[]
                    resultDisplay.textContent = cardsWon.length
                    cardsChosen=[]
                    cardsChosenIds=[]
                    createBoard()
                    console.log('le niveau choisi est le 3')
                    break
            }
            console.log(level)
        })
    }
}

